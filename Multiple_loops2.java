package pack2;

public class Multiple_loops2 {

	public static void main(String[] args) {
		System.out.println("even numbers from 1 to 20");
		for(char i='A'; i<='E'; i++)
		{
			for(char j='A'; j<='E'; j++)
			{
				System.out.print(j+" ");
			}
			System.out.println();
		}

	}

}
